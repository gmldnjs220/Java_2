public class Foo {
    //두 정수를 더하는 메소드
    public static int sum(int n, int m) {
        return n + m;
    }
    
    // main() 메소드에서 실행이 시작
    public static void main(String[] args) {

        int i = 20;
        int s;
        char a;

        s = sum(i, 10);
        a = '?';
        System.out.println(a);
        System.out.println("hello");
        System.out.println(s);
        
    }
}
